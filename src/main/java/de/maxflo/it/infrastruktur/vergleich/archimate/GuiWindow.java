    private void selectRefActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectRefActionPerformed

        final JFileChooser fc = new JFileChooser();
        int returnVal = fc.showOpenDialog(this);

        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            refLabel.setText(file.getName());
            refLabel.setForeground(new Color(0,150,0));
            reader.setRefFile(file);
        }
    }//GEN-LAST:event_selectRefActionPerformed

